char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	latest;

	i = 0;
	latest = -1;
	while (s[i])
	{
		if (s[i] == c)
			latest = i;
		i++;
	}
	if (s[i] == '\0' && c == '\0')
		return (char*)&s[i];
	else if (latest != -1)
		return (char*)&s[latest];
	return (0);
}
